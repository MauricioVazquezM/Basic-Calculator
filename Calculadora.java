    private void equalsBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsBtActionPerformed
        String cadena;
        cadena = infoTxt.getText();
        miCalculadora = new Calcu(cadena);
        if (cadena != "") {
            if (miCalculadora.verificacion() == false) {
                infoTxt.setText("ERROR"); // En caso de que la verificaci칩n de los parentesis este mal escrita la calculadora mandafra el mensaje de error
            } else {
                miCalculadora.infijoAPostfijo();
                infoTxt.setText(Double.toString(miCalculadora.evaluar()));
            }
        } else
            infoTxt.setText("");
    }//GEN-LAST:event_equalsBtActionPerformed

